package za.co.issuetracker.frontend.util;

/**
 * A util class for tje issue tracker UI
 *
 * @author Jaco Koekemoer
 * Date: 2023-02-27
 */
public class IssueTrackerUIUtil
{

    /**
     * Convert boolean to string
     *
     * @param value boolean
     * @return String
     */
    public static String convertBooleanToString(boolean value)
    {
        return value ? "Yes" : "No";
    }

}
